    private void rellenarFich (String fichero, JComboBox selector) throws IOException{
        
        LeerFichero fich = new LeerFichero(fichero);
        fich.readContent();
        String[] contenido = fich.getContent();
        
        for (int i=0; i < contenido.length; i++){
            selector.addItem(contenido[i]);
            
            
        }
        
        
    }
