    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        // TODO add your handling code here:
        
        // añadimos al selector los campos que metemos en las cajas de texto
        String nombre = cajaNombre.getText();
        selectorNombre.addItem(nombre);
        
        
        String arma = cajaArma.getText();
        selectorArma.addItem(arma);
        
      
        
        String habitacion = cajaHabitacion.getText();
        selectorHabitacion.addItem(habitacion);
        
        
       
        //mostramos el contenido en la caja acusacion
        
        
        if ((cajaArma.getText().isEmpty()) || (cajaNombre.getText().isEmpty()) || cajaHabitacion.getText().isEmpty())
        {
            String cadenaNombre = (String) selectorNombre.getSelectedItem();
            
           
            String cadenaArma = (String) selectorArma.getSelectedItem();
            
            String cadenahabitacion = (String) selectorHabitacion.getSelectedItem();
            
            
            cajaAcusacion.setText(cadenaNombre + " ha muerto con un/a " + cadenaArma + 
             " en el/la " + cadenahabitacion);
            
            cajaNombre.setText("");
            cajaArma.setText("");
            cajaHabitacion.setText("");
        }
        
        
        
    }//GEN-LAST:event_AceptarActionPerformed

    
